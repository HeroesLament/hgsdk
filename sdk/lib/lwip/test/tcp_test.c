       if (ret < 0) {
            printf("<---%s:Failed in connect to "HG_TCPIP_SERVER_IPADDR",ret:<%d>!\n", __FUNCTION__, ret);
            sys_msleep(1000);
            closesocket(clt_socket);
            continue;
       }

